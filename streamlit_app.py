# --- 3. 확률 직관 퀴즈 ---
st.header("⚡️ 당신의 직관은 얼마나 정확할까요? (확률 퀴즈)")
st.write("정답이 무엇일지 한번 추측해보세요!")

# 퀴즈 1: 생일 문제
st.subheader("🎂 퀴즈 1: 같은 생일 문제")
st.write("한 반에 몇 명의 학생이 있어야, 그중 생일이 같은 두 학생이 있을 확률이 50%를 넘을까요?")

birthday_guess = st.radio(
    "정답을 골라보세요!",
    ('50명', '100명', '23명', '183명'),
    key='birthday_q',
    horizontal=True
)

if st.button("생일 문제 정답 확인하기"):
    if birthday_guess == '23명':
        st.success("🎉 정답입니다! 생각보다 훨씬 적은 23명만 모여도 생일이 같은 쌍이 있을 확률이 50%를 넘어갑니다. 이를 '생일 문제'라고 부르며, 확률이 우리의 직관과 어떻게 다른지 보여주는 대표적인 예시입니다.")
    else:
        st.error(f"😯 아쉽네요! 정답은 **23명**입니다. 우리 생각보다 훨씬 적은 인원으로도 같은 생일이 나올 확률이 꽤 높답니다.")
st.markdown("---")


